#/t used for printing tabspace before string
tabby_cat = "\tI'm tabbed in."

#\n used to print the rest next line
persian_cat = "I'm split\non a line."

#two backslashed used to print single backslash
backslash_cat = "I'm \\ a \\ cat."

#""" for multiple lines \t for adding spaces
fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
"""
print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)